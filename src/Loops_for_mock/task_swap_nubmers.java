package Loops_for_mock;

public class task_swap_nubmers 
{
	public static void main(String[] args) 
	{ 
		// Swap the numbers 
		
		int a,b,c,d;
		
		a = 100;
		b = 1000;
		c = 10000;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
		System.out.println("after swap");
		
		d = a;
		a = c;
		c = d;
		
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		
	}

}
