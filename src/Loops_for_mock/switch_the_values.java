package Loops_for_mock;

public class switch_the_values 
{
	
	// swap the numbers
	
	static int a = 1;
	static int b = 2;	
	static int c;
	
	
	public static void main(String[] args) 
	{
		c = a ;       // c = 1 
		a = b ;    		// a = 2 
		b = c ;			//b = 1
		
		System.out.println(a);
		
		System.out.println(b);
		
		
	}

}
